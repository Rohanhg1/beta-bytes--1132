def seed_states_districts(db, State, District):
    """Seed all Indian states and districts."""
    if db.query(State).count() > 0:
        return

    for state_name, data in INDIA_STATES_DISTRICTS.items():
        state = State(state_name=state_name)
        db.add(state)
        db.flush()  # Get state_id

        for dist in data["districts"]:
            district = District(
                district_name=dist["name"],
                state_id=state.state_id,
                latitude=dist["lat"],
                longitude=dist["lon"],
            )
            db.add(district)

    db.commit()
    print(f"[SEED] Added {len(INDIA_STATES_DISTRICTS)} states with districts.")


def seed_hospitals(db, State, District, Hospital):
    """Seed hospital data."""
    if db.query(Hospital).count() > 0:
        return

    for h in HOSPITALS_DATA:
        state = db.query(State).filter(State.state_name == h["state"]).first()
        district = db.query(District).filter(District.district_name == h["district"]).first()

        if not state or not district:
            print(f"[WARN] Skipping hospital {h['name']}: state/district not found")
            continue

        hospital = Hospital(
            hospital_name=h["name"],
            specializations=h["specs"],
            district_id=district.district_id,
            state_id=state.state_id,
            district_name=h["district"],
            state_name=h["state"],
            latitude=h["lat"],
            longitude=h["lon"],
            available_doctors=h["doctors"],
            email=h.get("email", ""),
            rating=h["rating"],
            address=f"{h['district']}, {h['state']}",
        )
        db.add(hospital)

    db.commit()
    print(f"[SEED] Added {len(HOSPITALS_DATA)} hospitals.")
